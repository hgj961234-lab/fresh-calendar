    if (['대파', '쪽파', '고춧가루', '파'].some(k => n.includes(k)))
      return '❄️ 냉동실: 좌측 도어 (하)';

    // ❄️ [냉동] 우측 도어 (간식/가루/빵)
    // (상) 아이스크림, 작게 소분된 것
